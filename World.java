  private static void loadTextures() {
    if (unloaded) {
      try {
        wallTextures.add(
            ImageIO.read(
                new File(
                    "./wallTextures/1.png")));
        floorCeilingTextures.add(
            ImageIO.read(
                new File(
                    "./floorAndCeilingTextures/1.jpg")));
      } catch (Exception e) {
        System.out.println(e);
      }
      unloaded = false;
    }

    for (int i = 0; i < 24; i++) {
      for (int j = 0; j < 24; j++) {
        floorTexture[i][j] = 1;
      }
    }
  }

